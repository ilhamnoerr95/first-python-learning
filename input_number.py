# input number
# by default input() function is string data type
# untuk convert type data dari input kita bisa convert menggunakan int()

print("Input angka pertama: ")
a = int(input())

print("Input angka kedua: ")
b = int(input())

hasil = a + b
print(f"Hasil penjumlahan dari : {a} + {b} = {hasil}" )

